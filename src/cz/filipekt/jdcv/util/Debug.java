package cz.filipekt.jdcv.util;

/**
 * This class is used to specify, whether the application runs in debug mode.
 * It also holds some convenience data for debug mode.
 * 
 * @author Tomas Filipek <tom.filipek@seznam.cz>
 */
public class Debug {
	
	/**
	 * Marks whether the application runs in debugging mode
	 */
	public static final boolean debugModeOn = false;
	
	/**
	 * When in debug mode, this is used to specify the directory with 
	 * application resources.
	 */
	public static final String projectDir = "C:/diplomka/JDEECoVisualizer-master";
	
}
